'''
What is an f-string?

An f-string is a string with special syntax that allows the user to interpolate
Python expressions in the string. The f-string is indicated by an f preceding 
the string. Anything inside the braces in the f-string is interpolated.
'''

'''
What does this code print and why? What concept does this demonstrate?
'''

name = 'Abraham Lincoln'
print(f'{name} was a President of the US')

'''
This code prints 'Abraham Lincoln was a President of the US'

This demonstrates the use of an f-string. the value of the variable name is 
interpolated into the string when it is referenced inside the braces
'''

